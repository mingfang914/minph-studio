export default class I18n {
  constructor() {
    this.currentLang = localStorage.getItem("mp-portfolio-lang") || "en";
    this.buttons = document.querySelectorAll(".lang-btn");
    this.init();
  }

  init() {
    if (this.buttons.length === 0) return;

    // Set initial language without animation side effects
    this.updateDOM(this.currentLang);

    // Bind click events
    this.buttons.forEach((btn) => {
      btn.addEventListener("click", () => {
        const lang = btn.dataset.lang;
        if (lang && lang !== this.currentLang) {
          this.setLang(lang);
        }
      });
    });
  }

  setLang(lang) {
    this.currentLang = lang;
    localStorage.setItem("mp-portfolio-lang", lang);
    this.updateDOM(lang);

    // Dispatch custom event to notify other modules (like Work or Text Scramblers)
    const event = new CustomEvent("langchanged", {
      detail: { lang }
    });
    document.dispatchEvent(event);
  }

  updateDOM(lang) {
    // 1. Update active button classes
    this.buttons.forEach((btn) => {
      if (btn.dataset.lang === lang) {
        btn.classList.add("is-active");
      } else {
        btn.classList.remove("is-active");
      }
    });

    // 2. Update page elements with data-i18n
    const elements = document.querySelectorAll("[data-i18n]");
    const dict = translations[lang];

    elements.forEach((el) => {
      const key = el.getAttribute("data-i18n");
      const translation = dict[key];

      if (translation !== undefined) {
        // Safe to use innerHTML as translations are static developer-defined strings
        el.innerHTML = translation;
      }
    });

    // 3. Update HTML lang tag
    document.documentElement.lang = lang;
  }
}
